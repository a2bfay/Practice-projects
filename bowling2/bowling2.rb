require "test/unit"

class TestRollCalc < Test::Unit::TestCase
  def test_roll_limits
    roll = RollCalc.new
    assert roll.results.max <= 10
    assert roll.results.min >= 0
  end
  
  def test_roll_sum
    roll = RollCalc.new
    assert roll.results.reduce(:+).between? 0, 10
  end
    
  def test_roll_sum_skill
    skill = 15
    roll = RollCalc.new(skill)
    assert roll.results.reduce(:+).between? 0, 10
  end
end


