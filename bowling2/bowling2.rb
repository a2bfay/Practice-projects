# stores a set of rolls in an array
class Frame
  attr_reader :results  # need like this to rig frames for testing -- seems like a problem
 
  def initialize(roll_results)
    @results = roll_results
  end
  
  def total
    @results.reduce(:+)
  end

  def strike?
    @results[0] == 10
  end
  
  def spare?
    strike? == false && total == 10
  end
end
